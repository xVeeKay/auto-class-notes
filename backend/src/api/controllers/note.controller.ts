export const searchNotes = asyncHandler(
  async (req: AuthRequest, res: Response) => {
    const q = req.query.q as string;
    if (!q.trim()) {
      throw new apiError(400, "Search query is required");
    }
    const notes = await Note.find({
      userId: req.user!._id,
      $or: [
        {
          title: {
            $regex: q,
            $options: "i",
          },
        },
        {
          aiContent: {
            $regex: q,
            $options: "i",
          },
        },
      ],
    })
      .populate("subjectId", "title")
      .sort({ createdAt: -1 });

    res
      .status(200)
      .json(new apiResponse(true, "Notes fetched successfully", notes));
  },
);

export const retryNoteAI = asyncHandler(
  async (req: AuthRequest, res: Response) => {
    const note = await Note.findOne({
      userId: req.user!._id,
      _id: req.params.noteId,
    });
    if (!note) {
      throw new apiError(404, "Note not found");
    }
    if (note.status !== "failed") {
      throw new apiError(400, "Only failed notes can be retried");
    }
    note.status = "processing";
    await note.save();
    res
      .status(200)
      .json(new apiResponse(true, "Retry started", { noteId: note._id }));
  },
);
