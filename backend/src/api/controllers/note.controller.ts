export const getNotesBySubjects = asyncHandler(
  async (req: AuthRequest, res: Response) => {
    const { subjectId } = req.params;
    const page = Number(req.query.page) || 1;
    const limit = Number(req.query.limit) || 10;
    const { skip } = getPagination(page, limit);
    const notes = await Note.find({ userId: req.user!._id, subjectId })
      .sort({ createdBy: -1 })
      .skip(skip)
      .limit(limit)
      .lean()
      .populate("subjectId", "title");
    res
      .status(200)
      .json(new apiResponse(true, "Notes fetched successfully", notes));
    return;
  },
);

