export const editNote = asyncHandler(
  async (req: AuthRequest, res: Response) => {
    const note = await Note.findOne({
      _id: req.params.noteId,
      userId: req.user!._id,
    });

    if (!note) {
      throw new apiError(404, "Note not found");
    }

    note.editedContent = req.body.editedContent;
    note.isEdited = true;
    await note.save();

    res
      .status(200)
      .json(new apiResponse(true, "Note updated successfully", note));
    return;
  },
);

